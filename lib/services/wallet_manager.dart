class WalletManager extends ChangeNotifier {
  static const _storage = FlutterSecureStorage(
    aOptions: AndroidOptions(encryptedSharedPreferences: true),
    iOptions: IOSOptions(
      accessibility: KeychainAccessibility.first_unlock_this_device,
    ),
  );

  static const _keyPrefix = 'kitepay_wallet_';
  static const _walletListKey = 'kitepay_wallet_list';

  List<KiteWallet> _wallets = [];
  List<KiteWallet> get wallets => List.unmodifiable(_wallets);
  bool _loaded = false;

  // ── Load wallets from secure storage ─────────────────────────────────────
  Future<void> loadWallets() async {
    if (_loaded) return;
    try {
      final listJson = await _storage.read(key: _walletListKey);
      if (listJson == null) {
        _wallets = [];
        _loaded = true;
        notifyListeners();
        return;
      }
      final ids = List<String>.from(jsonDecode(listJson));
      final loaded = <KiteWallet>[];
      for (final id in ids) {
        final walletJson = await _storage.read(key: '$_keyPrefix$id');
        if (walletJson != null) {
          loaded.add(KiteWallet.fromJson(jsonDecode(walletJson)));
        }
      }
      _wallets = loaded;
      _loaded = true;
      notifyListeners();
    } catch (e) {
      debugPrint('loadWallets error: $e');
      _wallets = [];
      _loaded = true;
    }
  }

  // ── Generate a new EVM wallet ─────────────────────────────────────────────
  Future<KiteWallet> generateWallet({
    String name = 'KitePay Wallet',
  }) async {
    // Generate secure random private key
    final privateKeyBytes = _generateSecureRandomBytes(32);
    final privateKeyHex =
        privateKeyBytes.map((b) => b.toRadixString(16).padLeft(2, '0')).join();

    // Derive public key and address using web3dart
    final credentials = EthPrivateKey.fromHex(privateKeyHex);
    final address = credentials.address.hex;

    // Generate mnemonic-style backup phrase (12 words from entropy)
    final mnemonic = _generateMnemonic(privateKeyBytes);

    final wallet = KiteWallet(
      id: DateTime.now().millisecondsSinceEpoch.toString(),
      name: name,
      address: address,
      privateKeyHex: privateKeyHex,
      mnemonic: mnemonic,
      createdAt: DateTime.now(),
    );

    await _saveWallet(wallet);
    _wallets.add(wallet);
    notifyListeners();
    return wallet;
  }

  // ── Import wallet from private key ────────────────────────────────────────
  Future<KiteWallet?> importFromPrivateKey({
    required String privateKeyHex,
    String name = 'Imported Wallet',
  }) async {
    try {
      final clean = privateKeyHex.startsWith('0x')
          ? privateKeyHex.substring(2)
          : privateKeyHex;
      final credentials = EthPrivateKey.fromHex(clean);
      final address = credentials.address.hex;

      // Check for duplicate
      if (_wallets
          .any((w) => w.address.toLowerCase() == address.toLowerCase())) {
        return null;
      }

      final wallet = KiteWallet(
        id: DateTime.now().millisecondsSinceEpoch.toString(),
        name: name,
        address: address,
        privateKeyHex: clean,
        mnemonic: null,
        createdAt: DateTime.now(),
      );

      await _saveWallet(wallet);
      _wallets.add(wallet);
      notifyListeners();
      return wallet;
    } catch (e) {
      debugPrint('importFromPrivateKey error: $e');
      return null;
    }
  }

  // ── Import from mnemonic ──────────────────────────────────────────────────
  Future<KiteWallet?> importFromMnemonic({
    required String mnemonic,
    String name = 'Restored Wallet',
  }) async {
    try {
      final privateKeyHex = _mnemonicToPrivateKey(mnemonic.trim());
      return importFromPrivateKey(privateKeyHex: privateKeyHex, name: name);
    } catch (e) {
      debugPrint('importFromMnemonic error: $e');
      return null;
    }
  }

  // ── Delete wallet ─────────────────────────────────────────────────────────
  Future<void> deleteWallet(String id) async {
    await _storage.delete(key: '$_keyPrefix$id');
    _wallets.removeWhere((w) => w.id == id);
    await _saveWalletList();
    notifyListeners();
  }

  // ── Get private key for a wallet ──────────────────────────────────────────
  Future<String?> getPrivateKey(String walletId) async {
    final wallet = _wallets.firstWhere(
      (w) => w.id == walletId,
      orElse: () => throw Exception('Wallet not found'),
    );
    return wallet.privateKeyHex;
  }

  KiteWallet? get primaryWallet => _wallets.isEmpty ? null : _wallets.first;

  // ── Helpers ───────────────────────────────────────────────────────────────
  Future<void> _saveWallet(KiteWallet wallet) async {
    await _storage.write(
      key: '$_keyPrefix${wallet.id}',
      value: jsonEncode(wallet.toJson()),
    );
    await _saveWalletList();
  }

  Future<void> _saveWalletList() async {
    final ids = _wallets.map((w) => w.id).toList();
    await _storage.write(
      key: _walletListKey,
      value: jsonEncode(ids),
    );
  }

  Uint8List _generateSecureRandomBytes(int length) {
    final rng = Random.secure();
    return Uint8List.fromList(List.generate(length, (_) => rng.nextInt(256)));
  }

  // Simple 12-word mnemonic from entropy bytes
  String _generateMnemonic(Uint8List entropy) {
    const words = _wordList;
    final result = <String>[];
    for (var i = 0; i < 12; i++) {
      final idx = (entropy[i * 2] * 256 + entropy[i * 2 + 1]) % words.length;
      result.add(words[idx]);
    }
    return result.join(' ');
  }

  String _mnemonicToPrivateKey(String mnemonic) {
    final words = mnemonic.split(' ');
    final bytes = Uint8List(32);
    const wordList = _wordList;
    for (var i = 0; i < 12 && i < words.length; i++) {
      final idx = wordList.indexOf(words[i]);
      if (idx >= 0) {
        bytes[i * 2] = idx ~/ 256;
        bytes[i * 2 + 1] = idx % 256;
      }
    }
    // Fill remaining bytes deterministically
    for (var i = 24; i < 32; i++) {
      bytes[i] = bytes[i - 24] ^ 0xFF;
    }
    return bytes.map((b) => b.toRadixString(16).padLeft(2, '0')).join();
  }

